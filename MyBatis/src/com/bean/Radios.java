package com.bean;

public class Radios {
	private String radio;
	private String age;
	private String organ;
	private double organConteng;
	public String getRadio() {
		return radio;
	}
	public void setRadio(String radio) {
		this.radio = radio;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getOrgan() {
		return organ;
	}
	public void setOrgan(String organ) {
		this.organ = organ;
	}
	public double getOrganConteng() {
		return organConteng;
	}
	public void setOrganConteng(double organConteng) {
		this.organConteng = organConteng;
	}
	
}
