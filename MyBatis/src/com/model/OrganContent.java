package com.model;

public class OrganContent {
    
    private String radio;
    private String age;
    private String organ;
    private double organContent;
    public String getRadio() {
        return radio;
    }
    public void setRadio(String radio) {
        this.radio = radio;
    }
    public String getAge() {
        return age;
    }
    public void setAge(String age) {
        this.age = age;
    }
    public String getOrgan() {
        return organ;
    }
    public void setOrgan(String organ) {
        this.organ = organ;
    }
    public double getOrganContent() {
        return organContent;
    }
    public void setOrganContent(double organContent) {
        this.organContent = organContent;
    }
}