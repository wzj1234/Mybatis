package com.model;

public class SVTRAModel {
    
    private int id;
    private String radio;
    private String age;
    private double foodConsumeSum;
    private double SVTRA;    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getRadio() {
        return radio;
    }
    public void setRadio(String radio) {
        this.radio = radio;
    }
    public String getAge() {
        return age;
    }
    public void setAge(String age) {
        this.age = age;
    }
    public double getFoodConsumeSum() {
        return foodConsumeSum;
    }
    public void setFoodConsumeSum(double foodConsumeSum) {
        this.foodConsumeSum = foodConsumeSum;
    }
    public double getSVTRA() {
        return SVTRA;
    }
    public void setSVTRA(double SVTRA) {
        this.SVTRA = SVTRA;
    }
}

